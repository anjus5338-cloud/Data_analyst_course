#List:
list = ["Ram","Shyam"]
print(list)

#insert()
list = ["Ram","Shyam"]
list.insert(1,"Rajiv")
print(list)

#append():
list.append("Anil")
print(list)

#to update:
list[0] = "Ansh"
print(list)

#extend():
x = ["apple","banana","grapes"]
y = ["peas","carrot"]
x.extend(y)
print(x)
print(y)

#Remove():
x.remove("apple")
print(x)

#pop()
x.pop(1)
print(x)

#clear()
list.clear()
print(list)

#delete:
del list
print(list)